function greeting(name){
   return ("Hello "+ name + "!")
}

function multiplicationFunction(A,B){
product = A * B
return product
}
function divisionFunction(A,B){
divisionProduct = A / B
return divisionProduct
}
function additionFunction(A,B){
sumOfValues = A + B
return sumOfValues
}
function subtractionFunction(A,B){
subtractionSumOfValues = A - B
return subtractionSumOfValues
}
